import { findAllPosts } from "@/lib/post/admin";
import clsx from "clsx";
import Link from "next/link";
import { DeletePostButton } from "../DeletePostButton";
import ErrorMessage from "../../ErrorMessage";

export async function PostsListAdmin() {
  const posts = await findAllPosts();

  if (posts.length <= 0) {
    return (
      <ErrorMessage
        contentTitle="Epa ðŸ˜œ"
        content="Vamos por a mÃ£o na massa e criar novos posts?"
      />
    );
  }

  return (
    <div>
      {posts.map((post) => (
        <div
          key={post.id}
          className={clsx(
            "p-2 flex justify-between items-center gap-2",
            !post.published && "bg-slate-200"
          )}
        >
          <Link href={`/admin/post/${post.id}`}>{post.title}</Link>
          {!post.published && (
            <span className="text-xs text-slate-600">NÃ£o publicado</span>
          )}

          <DeletePostButton id={post.id} title={post.title} />
        </div>
      ))}
    </div>
  );
}
